/**
 * Nesse programa vamos usar o loop for para imprimir uma sequência
 * de númeo do 1 ao 10
 */
package aula6;

public class Contagem {
    
    public static void main(String args[]){

        for (int i = 1; i <= 10; i++){
            System.out.println("Numero: " + i);
        }

        System.out.println("=".repeat(30));
    }
}
