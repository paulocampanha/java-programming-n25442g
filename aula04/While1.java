/**
 * Nesse programa vamos estudar a estrutura de repetição while. Esse 
 * estrutura executa um bloco de código repetidamente enquanto uma 
 * condição específica for verdadeira
 */

package aula4;

public class While1 {

    public static void main(String args[]){

        int num = 1;
        while (num <= 10){
            System.out.println(num);
            num++;
        }
    }
    
}
